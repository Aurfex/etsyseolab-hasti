/*
📂 Module: dynamic-faq-ai.ts (Dynamic FAQ & Auto-Suggested Answers)
Purpose:
این ماژول به صورت پویا سوالات متداول (FAQ) و جواب‌های آن‌ها را بر اساس داده‌های واقعی (مانند پیام‌های مشتریان، جستجوهای پرتکرار، و تیکت‌های پشتیبانی) شناسایی کرده و تولید می‌کند. هدف اصلی آن، کاهش بار تیم پشتیبانی و ارائه پاسخ‌های سریع و دقیق به مشتریان است.

What it does:
۱. **اسکن و تحلیل داده‌ها:**
    - به صورت دوره‌ای یا با اجرای دستی، منابع داده (مانند دیتابیس پیام‌ها، آنالیتیکس جستجو) را اسکن می‌کند تا سوالات تکراری یا جدید را شناسایی کند.

۲. **پیشنهاد سوالات جدید:**
    - سوالات شناسایی‌شده را به عنوان "پیشنهاد" در داشبورد FAQ نمایش می‌دهد. هر پیشنهاد شامل متن سوال و منبع آن (مثلاً "Customer Message") است.

۳. **تولید پاسخ با هوش مصنوعی:**
    - مدیر می‌تواند برای هر سوال پیشنهادی، درخواست تولید پاسخ با AI را ارسال کند. هوش مصنوعی یک جواب کامل، ساختاریافته و با لحن مناسب فروشگاه تولید می‌کند.

۴. **تأیید و ویرایش انسانی (Human-in-the-Loop):**
    - پاسخ تولیدشده توسط AI قبل از انتشار، توسط مدیر قابل ویرایش و بازبینی است. هیچ محتوایی بدون تأیید نهایی منتشر نمی‌شود.

۵. **انتشار و مدیریت FAQ:**
    - پس از تأیید، سوال و جواب به لیست FAQهای عمومی وب‌سایت اضافه می‌شود. مدیران می‌توانند FAQهای موجود را ویرایش یا حذف کنند.

API Endpoints:
- `GET /api/faq` - دریافت لیست کامل FAQهای منتشرشده و سوالات پیشنهادی.
- `POST /api/faq` - انجام عملیات نوشتن مانند اسکن برای سوالات جدید، تولید پاسخ، و انتشار یا ویرایش FAQ.

Setup:
- نیازمند اتصال به منابع داده برای تحلیل (Data sources).
- پیکربندی لحن و سبک پاسخ‌های AI.
*/
